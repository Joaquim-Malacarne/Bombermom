const SPRITES = [SPR.pik, SPR.cha, SPR.squ, SPR.bul];

// Cache de sprites pré-renderizados
const SPRCC    = {};  // normal
const SPRCC_ME = {};  // com tint dourado (Mega Evolution)

function tintColor(hex, [tr, tg, tb, ta]) {
  const r = parseInt(hex.slice(1,3),16);
  const g = parseInt(hex.slice(3,5),16);
  const b = parseInt(hex.slice(5,7),16);
  const nr = Math.min(255, r + (tr-r)*ta);
  const ng = Math.min(255, g + (tg-g)*ta);
  const nb = Math.min(255, b + (tb-b)*ta);
  return `rgb(${nr|0},${ng|0},${nb|0})`;
}

function prerender(spr, tint) {
  const oc = document.createElement('canvas');
  oc.width = oc.height = 64;
  const ox = oc.getContext('2d');
  const ps = 4;
  for (let i = 0; i < 256; i++) {
    const ci = spr.px[i]; if (!ci) continue;
    let col = spr.pal[ci];
    if (tint) col = tintColor(col, tint);
    ox.fillStyle = col;
    const r = Math.floor(i/16), c = i%16;
    ox.fillRect(c*ps, r*ps, ps, ps);
  }
  return oc;
}

function initSprites() {
  [0,1,2,3].forEach(i => {
    SPRCC[i]    = prerender(SPRITES[i], null);
    SPRCC_ME[i] = prerender(SPRITES[i], [255,220,0,0.5]);
  });
}
